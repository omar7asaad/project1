// 2. إذا حذفت ملف widget، احذف الـ export الخاص به
//
// 3. يمكنك عمل exports مع إخفاء بعض الأشياء:
//    export 'file.dart' hide SomeClass;
//
// 4. يمكنك عمل exports مع إظهار أشياء محددة فقط:
//    export 'file.dart' show SomeClass, someFunction;
