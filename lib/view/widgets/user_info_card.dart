import 'package:flutter/material.dart';

// ============================================================
// UserInfoCard - الـ Widget الرئيسي
// ============================================================

// /// = تعليق توثيقي
// ⚙️ يمكن تغييره
/// بطاقة تعرض معلومات المستخدم

// class UserInfoCard = تعريف الكلاس
// ⚙️ يمكن تغيير الاسم
// extends StatelessWidget = بدون حالة
class UserInfoCard extends StatelessWidget {
  
  // ----------------------------------------------------------
  // المتغيرات (Properties)
  // ----------------------------------------------------------
  
  // final = ثابت
  // String = نص
  // mobile = رقم الموبايل
  // ⚙️ يمكن تغيير الاسم: phoneNumber, phone, etc.
  final String mobile;
  
  // accountStatus = حالة الحساب
  // ⚙️ يمكن تغيير الاسم: status, state, etc.
  final String accountStatus;

  // ----------------------------------------------------------
  // Constructor (المُنشئ)
  // ----------------------------------------------------------
  // const = ثابت (أفضل للأداء)
  const UserInfoCard({
    // Key? key = مفتاح اختياري
    // ⚙️ لا تغيّره
    Key? key,
    
    // required this.mobile = مطلوب (يجب تمريره)
    // ⚙️ يمكن إزالة required وإعطاء قيمة افتراضية
    required this.mobile,
    
    // this.accountStatus = 'مفعّل' = قيمة افتراضية
    // إذا لم يُمرر، يستخدم 'مفعّل'
    // ⚙️ يمكن تغيير القيمة الافتراضية
    this.accountStatus = 'مفعّل',
    
  }) : super(key: key);

  // ----------------------------------------------------------
  // build - بناء الواجهة
  // ----------------------------------------------------------
  @override
  Widget build(BuildContext context) {
    
    // Container = حاوية مع زخرفة
    return Container(
      
      // padding = المسافة الداخلية
      // EdgeInsets.all(15) = 15 بكسل من كل الجهات
      // ⚙️ يمكن تغييره
      padding: EdgeInsets.all(15),
      
      // --------------------------------------------------
      // decoration = الزخرفة
      // --------------------------------------------------
      decoration: BoxDecoration(
        
        // color = لون الخلفية
        // Color(0x1AFFFFFF) = أبيض 10% شفافية
        // 0x1A = 10% (من 00 إلى FF)
        // FFFFFF = أبيض
        // ⚙️ يمكن تغييره
        color: Color(0x1AFFFFFF),
        
        // borderRadius = زوايا مستديرة
        // ⚙️ يمكن تغييره: 0 لحاد، 30 لمستدير أكثر
        borderRadius: BorderRadius.circular(15),
        
        // border = الحدود
        // Border.all = حدود لكل الجهات
        border: Border.all(
          // color = لون الحدود
          // Color(0x4D64B5F6) = سماوي 30%
          // ⚙️ يمكن تغييره
          color: Color(0x4D64B5F6),
          
          // ⚙️ يمكن إضافة width:
          // width: 2,
        ),
      ),
      
      // --------------------------------------------------
      // child = المحتوى
      // --------------------------------------------------
      // Column = ترتيب عمودي
      child: Column(
        // children = العناصر الداخلية
        children: [
          
          // ================================================
          // صف رقم الموبايل
          // ================================================
          // _buildInfoRow = دالة مساعدة (معرّفة في الأسفل)
          // تُنشئ صف معلومات واحد
          // ⚙️ يمكن تغيير الأيقونة والنصوص
          _buildInfoRow(
            Icons.phone_rounded,  // الأيقونة
            'رقم الموبايل',       // التسمية
            mobile,               // القيمة (من المتغير)
          ),
          
          // ================================================
          // خط فاصل
          // ================================================
          // Divider = خط أفقي فاصل
          // ⚙️ يمكن تغييره أو حذفه
          Divider(
            // color = لون الخط
            // Colors.white24 = أبيض 24%
            // ⚙️ يمكن تغييره
            color: Colors.white24,
            
            // height = الارتفاع الكلي (مع المسافات)
            // ⚙️ يمكن تغييره
            height: 20,
            
            // ⚙️ يمكن إضافة:
            // thickness: 2,  // سماكة الخط نفسه
            // indent: 20,    // مسافة من اليسار
            // endIndent: 20, // مسافة من اليمين
          ),
          
          // ================================================
          // صف حالة الحساب
          // ================================================
          _buildInfoRow(
            Icons.verified_user_rounded,  // أيقونة موثق
            'حالة الحساب',                // التسمية
            accountStatus,                // القيمة (من المتغير)
          ),
          
          // ⚙️ يمكن إضافة صفوف أخرى:
          // Divider(color: Colors.white24, height: 20),
          // _buildInfoRow(Icons.email, 'البريد الإلكتروني', email),
          //
          // Divider(color: Colors.white24, height: 20),
          // _buildInfoRow(Icons.calendar_today, 'تاريخ التسجيل', date),
        ],
      ),
    );
  }

  // ============================================================
  // _buildInfoRow - دالة مساعدة لبناء صف معلومات
  // ============================================================
  // _ في البداية = private (خاصة بهذا الملف)
  // Widget = نوع الإرجاع
  // _buildInfoRow = اسم الدالة
  // المعاملات:
  //   - IconData icon = الأيقونة
  //   - String label = التسمية (مثل: رقم الموبايل)
  //   - String value = القيمة (مثل: 0512345678)
  // ⚙️ يمكن تغيير المعاملات حسب الحاجة
  
  /// بناء صف معلومات واحد
  Widget _buildInfoRow(IconData icon, String label, String value) {
    
    // Padding = يُضيف مسافة
    return Padding(
      // padding = المسافة
      // vertical: 6 = 6 بكسل أعلى وأسفل
      // ⚙️ يمكن تغييره
      padding: EdgeInsets.symmetric(vertical: 6),
      
      // Row = ترتيب أفقي
      child: Row(
        // children = العناصر
        children: [
          
          // --------------------------------------------------
          // القيمة (على اليسار للعربية)
          // --------------------------------------------------
          Text(
            // value.isNotEmpty ? value : '---'
            // إذا القيمة غير فارغة: اعرضها
            // وإلا: اعرض '---'
            // ⚙️ يمكن تغيير '---' لنص آخر: 'غير محدد', 'N/A'
            value.isNotEmpty ? value : '---',
            
            style: TextStyle(
              // color = لون النص
              // ⚙️ يمكن تغييره
              color: Colors.white,
              
              // fontSize = حجم الخط
              // ⚙️ يمكن تغييره
              fontSize: 14,
            ),
          ),
          
          // --------------------------------------------------
          // Spacer = مسافة مرنة
          // --------------------------------------------------
          // يأخذ كل المساحة المتبقية بين العناصر
          // يجعل القيمة على اليسار والتسمية على اليمين
          // ⚙️ يمكن استبداله بـ SizedBox(width: X) لمسافة ثابتة
          Spacer(),
          
          // --------------------------------------------------
          // التسمية
          // --------------------------------------------------
          Text(
            label,  // التسمية الممررة
            style: TextStyle(
              // Colors.white70 = أبيض 70% (أفتح من القيمة)
              // ⚙️ يمكن تغييره
              color: Colors.white70,
              fontSize: 13,
            ),
          ),
          
          // مسافة بين التسمية والأيقونة
          // ⚙️ يمكن تغييره
          SizedBox(width: 8),
          
          // --------------------------------------------------
          // الأيقونة (على اليمين)
          // --------------------------------------------------
          Icon(
            icon,  // الأيقونة الممررة
            
            // color = لون الأيقونة
            // Color(0xFF64B5F6) = سماوي (skyBlue)
            // ⚙️ يمكن تغييره
            color: Color(0xFF64B5F6),
            
            // size = حجم الأيقونة
            // ⚙️ يمكن تغييره
            size: 20,
          ),
        ],
      ),
    );
  }
}

