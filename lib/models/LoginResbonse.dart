// ============================================================
// LoginResponse.dart - نموذج رد تسجيل الدخول
// ============================================================
// هذا الملف يحتوي على كلاس LoginResponse
// يُستخدم لاستقبال رد الـ API عند تسجيل الدخول
// تحتوي على:
//   - token: رمز المصادقة (للوصول للـ API)
//   - message: رسالة من السيرفر
//   - status: حالة العملية (نجاح/فشل)
// ============================================================

// ============================================================
// LoginResponse - كلاس رد تسجيل الدخول
// ============================================================
class LoginResponse {
  
  // ----------------------------------------------------------
  // الخصائص
  // ----------------------------------------------------------
  // ? = nullable (يمكن أن يكون null)
  
  // رمز المصادقة (Token)
  // يُرسل مع كل طلب API لاحق
  // يُخزن محلياً (SharedPreferences مثلاً)
  String? token;
  
  // رسالة من السيرفر
  // مثال: "تم تسجيل الدخول بنجاح" أو "كلمة المرور خاطئة"
  String? message;
  
  // حالة العملية
  // 1 = نجاح
  // 0 = فشل
  // للتعديل: يمكن استخدام قيم أخرى حسب الـ API
  int? status;

  // ----------------------------------------------------------
  // Constructor - المُنشئ
  // ----------------------------------------------------------
  LoginResponse({
    this.token,
    this.message,
    this.status,
  });

  // ----------------------------------------------------------
  // fromJson - إنشاء LoginResponse من JSON
  // ----------------------------------------------------------
  // factory = مُنشئ خاص
  // يُستخدم لتحويل رد الـ API إلى كائن
  // 
  // مثال رد الـ API:
  // {
  //   "token": "eyJhbGciOiJIUzI1NiIsInR...",
  //   "message": "تم تسجيل الدخول بنجاح",
  //   "status": 1
  // }
  factory LoginResponse.fromJson(Map<String, dynamic> json) {
    return LoginResponse(
      token: json['token'],
      message: json['message'],
      status: json['status'],
    );
  }
}

